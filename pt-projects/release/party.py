def party():
    #print("Party Time!")
    for i in range(10):
        gled.on()
        buzz.on()
        sleep(0.5)
        gled.off()
        yled.on()
        sleep(0.5)
        buzz.off()
        yled.off()
        rled.on()
        sleep(0.5)
        buzz.on()
        rled.off()
        sleep(0.3)
        buzz.off()
        sleep(0.1)
    gled.on()
    yled.on()
    rled.on()
    buzz.on()
